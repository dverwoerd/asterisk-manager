    // ============================================================
    // QUEUES
    // ============================================================

    public function generateQueues(): bool
    {
        $queues = Database::fetchAll("SELECT * FROM queues WHERE enabled=1");
        $out    = $this->header('Queues Configuration');
        $out   .= "[general]\n\n";
        foreach ($queues as $queue) {
            $members = Database::fetchAll(
                "SELECT * FROM queue_members WHERE queue_id=? ORDER BY penalty",
                [$queue['id']]
            );
            $out .= $this->queueBlock($queue, $members);
        }
        return $this->writeFile('queues.conf', $out);
    }

    private function queueBlock(array $queue, array $members): string
    {
        $out  = '[' . $queue['name'] . "]\n";
        $out .= 'strategy=' . $queue['strategy'] . "\n";
        $out .= 'timeout=' . $queue['timeout'] . "\n";
        $out .= 'wrapuptime=' . $queue['wrapup_time'] . "\n";
        $out .= 'maxlen=' . $queue['max_callers'] . "\n";
        $out .= 'announce-holdtime=' . $queue['announce_hold_time'] . "\n";
        $out .= 'announce-position=' . $queue['announce_position'] . "\n";
        $out .= 'announce-frequency=' . $queue['announce_frequency'] . "\n";
        $out .= 'musicclass=' . $queue['music_on_hold'] . "\n";
        if (!empty($queue['join_announcement'])) {
            $out .= 'announce=' . $queue['join_announcement'] . "\n";
        }
        foreach ($members as $m) {
            $out .= 'member => PJSIP/' . $m['extension'] . ',penalty=' . ($m['penalty'] ?? 0) . "\n";
        }
        $out .= "\n";
        return $out;
    }

    // ============================================================
    // DIALPLAN (extensions.conf)
    // ============================================================

    public function generateDialplan(): bool
    {
        $out  = $this->header('Extensions (Dialplan)');
        $out .= $this->generateFromInternal();
        $out .= $this->generateFromTrunk();
        $out .= "[from-internal-custom]\n; Add custom rules here\n\n";
        $out .= "[from-trunk-custom]\n; Add custom rules here\n\n";
        $out .= $this->generateCustomContexts();
        return $this->writeFile('extensions.conf', $out);
    }

    private function generateFromInternal(): string
    {
        $extensions     = Database::fetchAll("SELECT * FROM extensions WHERE enabled=1");
        $ringGroups     = Database::fetchAll("SELECT * FROM ring_groups WHERE enabled=1");
        $queues         = Database::fetchAll("SELECT * FROM queues WHERE enabled=1");
        $outboundRoutes = Database::fetchAll("SELECT * FROM outbound_routes WHERE enabled=1 ORDER BY priority DESC");

        $out  = "[from-internal]\n";
        $out .= "include => from-internal-custom\n\n";

        // Extensies
        foreach ($extensions as $ext) {
            $exten    = $ext['extension'];
            $cidName  = $ext['callerid_name'] ?? '';
            $vmOn     = !empty($ext['voicemail_enabled']);
            $cfAlways = trim($ext['cf_always'] ?? '');
            $cfBusy   = trim($ext['cf_busy'] ?? '');
            $cfNoAns  = trim($ext['cf_noanswer'] ?? '');
            $cfVm     = !empty($ext['cf_voicemail']);
            $ringTime = (int)($ext['ring_time'] ?? 20);

            $out .= 'exten => ' . $exten . ",1,NoOp(Call to extension " . $exten . ")\n";
            $out .= ' same => n,Set(CALLERID(name)=' . $cidName . ")\n";

            // Altijd doorschakelen
            if (!empty($cfAlways)) {
                $out .= ' same => n,Dial(Local/' . $cfAlways . "@from-internal,60,tT)\n";
                $out .= " same => n,Hangup()\n\n";
                continue;
            }

            // Normaal bellen met busy/noanswer handling
            $dialOpts = 'tTr';
            if (!empty($cfBusy)) $dialOpts .= 'j'; // j = jump on busy
            $out .= ' same => n,Dial(PJSIP/' . $exten . ',' . $ringTime . ',' . $dialOpts . ")\n";

            // Bij bezet
            if (!empty($cfBusy)) {
                $out .= ' same => n(busy),Dial(Local/' . $cfBusy . "@from-internal,60,tT)\n";
            }

            // Bij geen antwoord - extern of voicemail
            if (!empty($cfNoAns)) {
                $out .= ' same => n,Dial(Local/' . $cfNoAns . "@from-internal,60,tT)\n";
            } elseif ($cfVm && $vmOn) {
                $out .= ' same => n,VoiceMail(' . $exten . "@default,u)\n";
            } elseif ($vmOn) {
                $out .= ' same => n,VoiceMail(' . $exten . "@default,u)\n";
            }

            $out .= " same => n,Hangup()\n\n";
        }

        // Ring Groups
        foreach ($ringGroups as $rg) {
            $members   = Database::fetchAll(
                "SELECT * FROM ring_group_members WHERE ring_group_id=? ORDER BY order_num",
                [$rg['id']]
            );
            $dialParts = array_map(fn($m) => 'PJSIP/' . $m['extension'], $members);
            $dialStr   = implode('&', $dialParts);
            $cidPrefix = $rg['caller_id_prefix'] ?? '';
            $out .= 'exten => ' . $rg['number'] . ",1,NoOp(Ring Group: " . $rg['name'] . ")\n";
            if ($cidPrefix) {
                $out .= ' same => n,Set(CALLERID(name)=' . $cidPrefix . '${CALLERID(name)}' . ")\n";
            }
            $out .= ' same => n,Dial(' . $dialStr . ',' . $rg['ring_time'] . ",tTr)\n";
            $out .= $this->destinationLine($rg['destination_type'], $rg['destination']);
            $out .= " same => n,Hangup()\n\n";
        }

        // Queues
        foreach ($queues as $q) {
            $out .= 'exten => ' . $q['number'] . ",1,NoOp(Queue: " . $q['name'] . ")\n";
            if (!empty($q['join_announcement'])) {
                $out .= ' same => n,Playback(' . $q['join_announcement'] . ")\n";
            }
            $out .= ' same => n,Queue(' . $q['name'] . ",tT)\n";
            $out .= $this->destinationLine($q['timeout_destination_type'], $q['timeout_destination']);
            $out .= " same => n,Hangup()\n\n";
        }

        // Voicemail toegang
        $out .= "exten => *98,1,NoOp(Voicemail Access)\n";
        $out .= ' same => n,VoiceMailMain(${CALLERID(num)}@default)' . "\n";
        $out .= " same => n,Hangup()\n\n";

        // Uitgaande routes
        foreach ($outboundRoutes as $route) {
            $patterns = Database::fetchAll(
                "SELECT * FROM outbound_route_dial_patterns WHERE route_id=?",
                [$route['id']]
            );
            $trunks = Database::fetchAll(
                "SELECT t.* FROM trunks t
                 JOIN outbound_route_trunks ort ON t.id=ort.trunk_id
                 WHERE ort.route_id=? AND t.enabled=1
                 ORDER BY ort.order_num",
                [$route['id']]
            );
            if (empty($trunks)) continue;
            $trunkName = $trunks[0]['name'];

            foreach ($patterns as $p) {
                $match    = trim($p['match_pattern']);
                $prepend  = trim($p['prepend'] ?? '');
                $prefix   = trim($p['prefix'] ?? '');
                $stripLen = strlen($prefix);
                if (empty($match)) continue;

                $out .= 'exten => ' . $match . ',1,NoOp(Outbound: ' . $route['name'] . ")\n";
                if ($prepend !== '' || $prefix !== '') {
                    $extenVar = '${EXTEN:' . $stripLen . '}';
                    $out .= ' same => n,Set(OUTNUM=' . $prepend . $extenVar . ")\n";
                    $out .= ' same => n,Dial(PJSIP/${OUTNUM}@' . $trunkName . ",60,tT)\n";
                } else {
                    $out .= ' same => n,Dial(PJSIP/${EXTEN}@' . $trunkName . ",60,tT)\n";
                }
                $out .= " same => n,Hangup()\n\n";
            }
        }

        return $out;
    }

    private function generateFromTrunk(): string
    {
        $routes = Database::fetchAll("SELECT * FROM inbound_routes WHERE enabled=1 ORDER BY priority DESC");
        $out    = "[from-trunk]\n";
        $out   .= "include => from-trunk-custom\n\n";

        foreach ($routes as $route) {
            $did  = !empty($route['did']) ? $route['did'] : 's';
            $out .= 'exten => ' . $did . ",1,NoOp(Inbound DID: " . $did . ")\n";
            $out .= " same => n,Answer()\n";
            $out .= $this->destinationLine($route['destination_type'], $route['destination']);
            $out .= " same => n,Hangup()\n\n";
        }

        // Catch-all
        $out .= "exten => _.,1,NoOp(Unknown inbound)\n";
        $out .= " same => n,Hangup(21)\n\n";

        return $out;
    }

    private function destinationLine(string $type, string $destination): string
    {
        $dest = $destination;
        return match ($type) {
            'extension'    => " same => n,Goto(from-internal," . $dest . ",1)\n",
            'queue'        => " same => n,Queue(" . $dest . ",tT)\n",
            'ring_group'   => " same => n,Goto(from-internal," . $dest . ",1)\n",
            'voicemail'    => " same => n,VoiceMail(" . $dest . "@default,u)\n",
            'external'     => " same => n,Dial(Local/" . $dest . "@from-internal,60,tT)\n",
            'announcement' => " same => n,Playback(" . $dest . ")\n same => n,Hangup()\n",
            default        => " same => n,Hangup()\n",
        };
    }

    private function generateCustomContexts(): string
    {
        $contexts = Database::fetchAll("SELECT * FROM dialplan_contexts");
        $out      = '';
        foreach ($contexts as $ctx) {
            $entries = Database::fetchAll(
                "SELECT * FROM dialplan_entries WHERE context_id=? ORDER BY extension, priority",
                [$ctx['id']]
            );
            if (empty($entries)) continue;
            $out .= '[' . $ctx['name'] . "]\n";
            if ($ctx['description']) {
                $out .= '; ' . $ctx['description'] . "\n";
            }
            foreach ($entries as $e) {
                $out .= 'exten => ' . $e['extension'] . ',' . $e['priority'] . ',';
                $out .= $e['application'] . '(' . ($e['app_data'] ?? '') . ")\n";
            }
            $out .= "\n";
        }
        return $out;
    }

    // ============================================================
    // RELOAD
    // ============================================================

    public function reload(?string $module = null): bool
    {
        $ami = AsteriskAMI::fromSettings();
        if (!$ami->connect()) {
            $cmd = '/usr/sbin/asterisk -rx "core reload"';
            exec($cmd . ' 2>&1', $output, $code);
            return $code === 0;
        }
        $result = $ami->reload($module ?? '');
        $ami->disconnect();
        return $result;
    }
