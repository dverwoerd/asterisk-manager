<?php
// ============================================================
// AsteriskConfig - Generates Asterisk config files
// Asterisk 22 / PJSIP - Inbound/Outbound split
// ============================================================

class AsteriskConfig
{
    private string $configPath;

    public function __construct(string $configPath = '/etc/asterisk')
    {
        $this->configPath = rtrim($configPath, '/');
    }

    public static function fromSettings(): self
    {
        return new self(Database::getSetting('asterisk_config_path', '/etc/asterisk'));
    }

    private function writeFile(string $filename, string $content): bool
    {
        $path = $this->configPath . '/' . $filename;
        if (file_exists($path)) {
            copy($path, $path . '.bak.' . date('YmdHis'));
        }
        $result = file_put_contents($path, $content);
        if ($result === false) {
            logError("Failed to write: $path");
            return false;
        }
        chmod($path, 0640);
        return true;
    }

    private function header(string $title): string
    {
        $line = str_repeat(';', 60);
        $date = date('Y-m-d H:i:s');
        return ";$line\n; $title\n; Generated by Asterisk Manager on $date\n; DO NOT EDIT MANUALLY\n;$line\n\n";
    }

    // ============================================================
    // PJSIP
    // ============================================================

    public function generatePJSIP(): bool
    {
        $trunks     = Database::fetchAll("SELECT * FROM trunks WHERE enabled=1 AND type='pjsip'");
        $extensions = Database::fetchAll("SELECT * FROM extensions WHERE enabled=1");

        $externalHost = Database::getSetting('asterisk_external_host', '');
        $localNets    = Database::getSetting('asterisk_local_nets', '192.168.0.0/16,172.16.0.0/12,10.0.0.0/8');

        $out  = $this->header('PJSIP Configuration');

        // Transport UDP
        $out .= "[transport-udp]\n";
        $out .= "type=transport\n";
        $out .= "protocol=udp\n";
        $out .= "bind=0.0.0.0\n";
        if (!empty($externalHost)) {
            $out .= "external_media_address=" . $externalHost . "\n";
            $out .= "external_signaling_address=" . $externalHost . "\n";
            foreach (explode(',', $localNets) as $net) {
                $net = trim($net);
                if ($net) {
                    $out .= "local_net=" . $net . "\n";
                }
            }
        }
        $out .= "\n";

        // Transport TCP
        $out .= "[transport-tcp]\n";
        $out .= "type=transport\n";
        $out .= "protocol=tcp\n";
        $out .= "bind=0.0.0.0\n\n";

        // Extensions
        foreach ($extensions as $ext) {
            $out .= $this->pjsipEndpoint($ext);
        }

        // Trunks
        foreach ($trunks as $trunk) {
            $type = $trunk['trunk_type'] ?? 'provider';
            if ($type === 'pbx') {
                $out .= $this->pjsipTrunkPBX($trunk);
            } else {
                $out .= $this->pjsipTrunk($trunk);
            }
        }

        return $this->writeFile('pjsip.conf', $out);
    }

    private function pjsipEndpoint(array $ext): string
    {
        $exten     = $ext['extension'];
        $secret    = $ext['secret'] ?? '';
        $context   = $ext['context'] ?? 'from-internal';
        $cidName   = $ext['callerid_name'] ?? '';
        $cidNum    = $ext['callerid_number'] ?? '';
        $maxCon    = (int)($ext['max_contacts'] ?? 1);
        $dtmf      = $ext['dtmf_mode'] ?? 'rfc4733';
        $recording = $ext['call_recording'] ?? 'never';
        $vmEnabled = !empty($ext['voicemail_enabled']);
        $codecs    = $ext['codecs'] ?? 'ulaw,alaw,g722';
        $out       = '';

        // Asterisk 22: alle secties hebben dezelfde naam, type= maakt het onderscheid
        $out .= '[' . $exten . "]\n";
        $out .= "type=auth\n";
        $out .= "auth_type=userpass\n";
        $out .= 'username=' . $exten . "\n";
        $out .= 'password=' . $secret . "\n\n";

        $out .= '[' . $exten . "]\n";
        $out .= "type=aor\n";
        $out .= 'max_contacts=' . $maxCon . "\n";
        $out .= "remove_existing=yes\n";
        $out .= "qualify_frequency=30\n";
        if ($vmEnabled) {
            $out .= 'mailboxes=' . $exten . "@default\n";
        }
        $out .= "\n";

        $out .= '[' . $exten . "]\n";
        $out .= "type=endpoint\n";
        $out .= 'context=' . $context . "\n";
        $out .= 'auth=' . $exten . "\n";
        $out .= 'aors=' . $exten . "\n";
        $out .= 'callerid=' . $cidName . ' <' . $cidNum . ">\n";
        $out .= 'allow=' . $codecs . "\n";
        $out .= 'dtmf_mode=' . $dtmf . "\n";
        $out .= "direct_media=no\n";
        $out .= "ice_support=no\n";
        // ACL - standaard alleen intern netwerk
        $out .= "deny=0.0.0.0/0.0.0.0\n";
        $out .= "permit=172.16.0.0/12\n";
        $out .= "permit=10.0.0.0/8\n";
        $out .= "permit=192.168.0.0/16\n";
        if (!empty($ext['allowed_ips'])) {
            foreach (array_filter(array_map('trim', explode(",", $ext['allowed_ips']))) as $allowedIp) {
                $out .= "permit=" . $allowedIp . "\n";
            }
        }
        // Max 2 gelijktijdige gesprekken per extensie
        $out .= "max_audio_streams=2\n";
        if ($recording === 'always') {
            $out .= "record_calls=yes\n";
        }
        $out .= "\n";

        return $out;
    }

    private function pjsipTrunk(array $trunk): string
    {
        $name       = $trunk['name'];
        $host       = $trunk['host'];
        $port       = $trunk['port'] ?? 5060;
        $username   = $trunk['username'];
        $password   = $trunk['password'];
        $context    = $trunk['context'] ?? 'from-trunk';
        $codecs     = $trunk['codecs'] ?? 'ulaw,alaw,g722';
        $outboundCid = $trunk['outbound_cid'] ?? '';
        $resolvedIp = gethostbyname($host);
        $out        = '';

        // ---- Uitgaand: met authenticatie ----
        $out .= "; === Trunk: " . $name . " (outbound) ===\n";

        $out .= "[reg-" . $name . "]\n";
        $out .= "type=registration\n";
        $out .= "transport=transport-udp\n";
        $out .= "outbound_auth=" . $name . "-auth\n";
        $out .= "server_uri=sip:" . $host . ":" . $port . "\n";
        $out .= "client_uri=sip:" . $username . "@" . $host . ":" . $port . "\n";
        $out .= "contact_user=" . $username . "\n";
        $out .= "retry_interval=60\n";
        $out .= "expiration=3600\n\n";

        $out .= "[" . $name . "-auth]\n";
        $out .= "type=auth\n";
        $out .= "auth_type=userpass\n";
        $out .= "username=" . $username . "\n";
        $out .= "password=" . $password . "\n\n";

        $out .= "[" . $name . "-aor]\n";
        $out .= "type=aor\n";
        $out .= "contact=sip:" . $username . "@" . $host . ":" . $port . "\n\n";

        $out .= "[" . $name . "]\n";
        $out .= "type=endpoint\n";
        $out .= "transport=transport-udp\n";
        $out .= "context=" . $context . "\n";
        $out .= "aors=" . $name . "-aor\n";
        $out .= "auth=" . $name . "-auth\n";
        $out .= "outbound_auth=" . $name . "-auth\n";
        $out .= "allow=" . $codecs . "\n";
        // from_user weggelaten zodat Asterisk het CallerID van de extensie gebruikt
        $out .= "from_domain=" . $host . "\n";
        $out .= "direct_media=no\n";
        $out .= "rtp_symmetric=yes\n";
        $out .= "force_rport=yes\n";
        $out .= "rewrite_contact=yes\n";
        if (!empty($outboundCid)) {
            $out .= "callerid=" . $outboundCid . " <" . $outboundCid . ">\n";
        }
        $out .= "send_pai=yes\n";
        $out .= "send_rpid=yes\n";
        $out .= "\n";

        // ---- Inkomend: geen authenticatie, IP-gebaseerd ----
        $out .= "; === Trunk: " . $name . " (inbound) ===\n";

        $out .= "[" . $name . "-inbound]\n";
        $out .= "type=endpoint\n";
        $out .= "transport=transport-udp\n";
        $out .= "context=" . $context . "\n";
        $out .= "allow=" . $codecs . "\n";
        $out .= "direct_media=no\n";
        $out .= "rtp_symmetric=yes\n";
        $out .= "force_rport=yes\n";
        $out .= "rewrite_contact=yes\n";
        $out .= "trust_id_inbound=yes\n\n";

        $out .= "[" . $name . "-inbound]\n";
        $out .= "type=aor\n";
        $out .= "contact=sip:" . $host . ":" . $port . "\n\n";

        $out .= "[" . $name . "-inbound]\n";
        $out .= "type=identify\n";
        $out .= "endpoint=" . $name . "-inbound\n";
        $out .= "match=" . $host . "\n";
        if (!empty($resolvedIp) && $resolvedIp !== $host) {
            $out .= "match=" . $resolvedIp . "\n";
        }
        $out .= "\n";

        return $out;
    }

